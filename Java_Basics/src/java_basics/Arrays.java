package java_basics;

public class Arrays {

	public static void main(String[] args) {
		
		int [] values = new int[100];
		values[0] = 1000;
		values[1] = 2000;
		System.out.println(values[1]);  //2000
		
		String [] words = new String[] {"Hello", "World"};
		System.out.print(words[1]); // World

	}

}
